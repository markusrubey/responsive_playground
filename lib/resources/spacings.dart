class Spacings {
  /// 0.0 dp
  static const zero = 0.0;

  /// 4.0 dp
  static const xsmall = 4.0;

  /// 8.0 dp
  static const small = 8.0;

  /// 12.0 dp
  static const medium = 12.0;

  /// 16.0 dp
  static const large = 16.0;

  /// 24.0 dp
  static const xlarge = 24.0;

  /// 32.0 dp
  static const xxlarge = 32.0;

  @override
  String toString() => 'xsmall: $xsmall\n'
      'small: $small\n'
      'medium: $medium\n'
      'large: $large\n'
      'xlarge: $xlarge\n'
      'xxlarge: $xxlarge';
}
